def real_ip_info(ip):
    try:
        # First API (fast & free)
        r1 = requests.get(f"http://ip-api.com/json/{ip}?fields=66846719", timeout=10)
        data1 = r1.json()

        if data1["status"] == "fail":
            raise Exception

        # Second API (backup + more fields)
        r2 = requests.get(f"https://ipwho.is/{ip}", timeout=10)
        data2 = r2.json()

        return data1, data2

    except:
        return None, None

