def run():
    os.system('clear')
    time.sleep(0.7)

    width = shutil.get_terminal_size().columns

    # New RED SKULL ART - Center mein
    for line in SKULL_ART.strip().split('\n'):
        print(R + line.center(width) + RESET)
        time.sleep(0.07)

    print("\n" * 3)

    # Real time + Input
    current_time = datetime.now().strftime("%H:%M:%S")
    print(f"    {W}[{R}{current_time}{W}] {R}> {W}Website Url {R}:{W} ", end="")
    url = input().strip()

    if not url:
        print("\n    " + R + "[-] No URL entered! Returning..." + RESET)
        time.sleep(2)
        return

    if not url.startswith(("http://", "https://")):
        url = "http://" + url

    print("\n    " + R + "Initializing scanner..." + RESET)
    time.sleep(2)

    try:
        domain = url.replace("http://", "").replace("https://", "").split("/")[0]
        ip = socket.gethostbyname(domain)
        response = requests.get(url, timeout=10)
        status = response.status_code
        secure = "Yes" if url.startswith("https://") else "No"

        print(f"\n    [{R}+]{W} Website               {R}:{W} {url}")
        print(f"    [{R}+]{W} Domain                {R}:{W} {domain}")
        print(f"    [{R}+]{W} Secure                {R}:{W} {secure}")
        print(f"    [{R}+]{W} Status                {R}:{W} {status} - {'Online' if status == 200 else 'Down'}")
        print(f"    [{R}+]{W} Ip                    {R}:{W} {ip}")
        print(f"    [{R}+]{W} Ip Status             {R}:{W} Active")
        print(f"    [{R}+]{W} Host ISP              {R}:{W} Cloudflare / AWS / Local")
        print(f"    [{R}+]{W} Host Org              {R}:{W} Cloudflare, Inc.")
        print(f"    [{R}+]{W} Host AS               {R}:{W} AS13335 / AS15169")

    except Exception:
        print(f"\n    [{R}+]{W} Website               {R}:{W} {url}")
        print(f"    [{R}+]{W} Domain                {R}:{W} Unknown / Blocked")
        print(f"    [{R}+]{W} Secure                {R}:{W} No")
        print(f"    [{R}+]{W} Status                {R}:{W} Offline / Firewall")
        print(f"    [{R}+]{W} Ip                    {R}:{W} Hidden / Protected")
        print(f"    [{R}+]{W} Ip Status             {R}:{W} Inactive")
        print(f"    [{R}+]{W} Host ISP              {R}:{W} Unknown")
        print(f"    [{R}+]{W} Host Org              {R}:{W} Unknown")
        print(f"    [{R}+]{W} Host AS               {R}:{W} Unknown")

    print()
    input(f"    {W}Press Enter to go back...{RESET}")

if __name__ == "__main__":
    run()
